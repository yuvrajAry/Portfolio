// Fetch Live Jobs from Adzuna
export const fetchLiveJobs = async (keyword = 'developer', location = '') => {
    if (ADZUNA_APP_ID === 'YOUR_APP_ID') {
        console.warn("Adzuna API Keys missing. Using simulated data.");
        return null; // Trigger fallback
    }

    try {
        const url = `${ADZUNA_BASE_URL}?app_id=${ADZUNA_APP_ID}&app_key=${ADZUNA_APP_KEY}&what=${keyword}&where=${location}&content-type=application/json`;
        const response = await fetch(url);
        if (!response.ok) throw new Error('Adzuna API failed');
        const data = await response.json();
        return data.results;
    } catch (error) {
        console.error("Adzuna Fetch Error:", error);
        return null;
    }
};

